package main.java.testName.alg;

public class Score {
	public Score(int techIn, int culIn, int totalIn){
		tech=techIn;
		cul=culIn;
		total=totalIn;
	}
	
	private int tech;
	private int cul;
	private int total;
	public int getTech() {
		return tech;
	}
	public int getCul() {
		return cul;
	}
	public int getTotal() {
		return total;
	}
	
	
}
